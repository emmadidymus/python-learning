# The upper() method returns the string in upper case
a = "Hello, World!"
print(a.upper())

# The lower() methode returns the string in lower case
print(a.lower())

# The strip() method removes any whitespace from the beginning or the end
b = " Hello, World! "
print(b.strip())

# The replace() method replaces one string with another
print(a.replace("H", "J"))

# The split() method splits a string into substrings if it finds instances of a separator
print(a.split("o"))